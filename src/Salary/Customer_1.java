    private void txtCusSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCusSearchKeyReleased
        // TODO add your handling code here:
        String val=txtCusSearch.getText().toString();
         
        int a;
        try {
            pst = con.prepareStatement("select * from customer where customerPno like '%"+val+"%'");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rd = rs.getMetaData();
            a = rd.getColumnCount();
            df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                
                Vector v2 = new Vector();
                for(int i=1 ; i<=a ; i++){
                    v2.add(rs.getString("customerId"));
                    v2.add(rs.getString("customerFName"));
                    v2.add(rs.getString("customerLName"));
                    v2.add(rs.getString("customerNIC"));
                    v2.add(rs.getString("customerPno"));
                    v2.add(rs.getString("customerEmail"));
                    v2.add(rs.getString("customerAddress"));
                    v2.add(rs.getString("customerDiscount"));
                     
                }
                df.addRow(v2);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Customer_1.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_txtCusSearchKeyReleased

